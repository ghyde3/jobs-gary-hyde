// Embeds the public profile markdown (which lives OUTSIDE this repo, in the
// jobseek workspace root) into a committed TS module so Vercel builds never need it.
// The public profile is recruiter-safe: no salary numbers, no scoring rubrics,
// no internal agent instructions. The master profile is never fed to the site.
// Rerun with `npm run knowledge` whenever the public profile changes.
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';

const src = path.resolve(process.cwd(), '../Gary_Hyde_Public_Profile.md');
const dest = path.resolve(process.cwd(), 'app/data/knowledge.ts');

const md = readFileSync(src, 'utf8');
const out =
  '// Generated by scripts/build-knowledge.mjs. Do not edit by hand.\n' +
  '// Rerun `npm run knowledge` after editing ../Gary_Hyde_Public_Profile.md.\n' +
  `export const MASTER_PROFILE = ${JSON.stringify(md)};\n`;

writeFileSync(dest, out);
console.log(`wrote ${dest} (${md.length} chars)`);
